# ### Problem 4: List to Tuple Conversion

# **Description:** Write a Python program that converts a list of numbers into a tuple, then prints the tuple.

# **Sample Input:** `[5, 10, 7, 4, 15, 3]`

# **Sample Output:** `(5, 10, 7, 4, 15, 3)`

